package lesson_2;

public class IfExample {
    public static void main(String[] args) {

        int a = 5;
        if (a > 25) {
            System.out.println("a > 25");
        } else {
            System.out.println("a is small");
        }

        int b = 10;
        if (b > 25) {
            System.out.println("b > 25");
        }
        if (b < 20) {
            System.out.println("b < 20");
        }

    }
}
